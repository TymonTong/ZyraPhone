const MIN_H = 24

const bars = raw
  .trim()
  .split('\n')
  .map((line) => {
    const [x, y, , h] = line.split(',').map(Number)
    if (h < MIN_H) return null
    const relX = (x - GX) / GW
    const bottomInGroup = (y - GY + h) / GH
    return {
      left: +(INSET_L + relX * GW_PCT).toFixed(3),
      bottom: +(INSET_B + (1 - bottomInGroup) * GH_PCT).toFixed(3),
      height: +((h / GH) * GH_PCT).toFixed(3),
    }
  })
  .filter(Boolean)

